public class Pyramid extends Pattern
{
    public void size(int n)
    { 
        int i,j;
        for (int i=0; i<n; i++)
            { 
                System.out.print(" "); 
            }  
            for (int j=0;j<=i; j++ ) 
            { 
                System.out.print("* ");
                
        
        }
        
        }
        
    }
        

